package tree_sitter_liotree_test

import (
	"testing"

	tree_sitter "github.com/tree-sitter/go-tree-sitter"
	tree_sitter_liotree "github.com/tree-sitter/tree-sitter-liotree/bindings/go"
)

func TestCanLoadGrammar(t *testing.T) {
	language := tree_sitter.NewLanguage(tree_sitter_liotree.Language())
	if language == nil {
		t.Errorf("Error loading Lovdog Directory Tree Format grammar")
	}
}
